window.LANG = 'zh';
window.setLang = function (l) {
  if (!window.I18N[l]) return;
  window.LANG = l;
  document.documentElement.lang = l === 'zh' ? 'zh-Hant' : l;
  if (typeof window.onLangChange === 'function') window.onLangChange();
};
window.T = function (key) {
  const dict = window.I18N[window.LANG] || window.I18N.zh;
  return dict[key] !== undefined ? dict[key] : key;
};
/* 取多語物件的目前語言字串 / pick current-lang string from a {zh,en,es} object */
window.L = function (obj) {
  if (!obj) return '';
  return obj[window.LANG] !== undefined ? obj[window.LANG] : (obj.zh || '');
};
